<div class="flex flex-col gap-4 text-justify">
  <h1
    class="saira text-2xl uppercase mb-4 pb-1 border-b-2 border-solid border-pagodapurple-shine"
  >
    What is Neocities Pagoda?
  </h1>
  <p>
    <em>Neocities Pagoda</em> is a central hub for all things Neocities. It's a place
    where you engage with the community and connect with other users who share similar
    interests.
  </p>
  <p>
    <em>Neocities Pagoda</em> has the following things to offer to its users:
  </p>
  <ul class="ml-8 flex flex-col gap-4 mt-4">
    <li>
      <b class="underline decoration-dotted">Guilds</b>: Guilds are the public
      groups dedicated to a specific type of community, theme, or topic. Guilds
      can also be your own little corner of the internet where you and your
      friends can hang out. You can create a guild just for chatting and fun.
      Guilds are comprised of the following entities:
      <ul class="ml-8 flex flex-col gap-4 mt-4">
        <li>
          <b class="underline decoration-dotted">Guild Owner</b>: A guild owner
          is a person who created the guild. Ownerships can be transferred to
          other <b class="underline decoration-dotted">guild members</b>. There
          can be only one owner of a guild at a time.
        </li>
        <li>
          <b class="underline decoration-dotted">Guild Members</b>: Guild
          members are the users who have joined the guild. Members can be
          assigned various <b class="underline decoration-dotted">roles</b> based
          on heirarchy. You can only be a member of 100 guilds at a time.
        </li>
        <li>
          <b class="underline decoration-dotted">Guild Roles</b>: Guild roles
          are the positions assigned to the
          <b class="underline decoration-dotted">guild members</b>
          based on heirarchy. There can be multiple roles in a
          <b class="underline decoration-dotted">guild</b>. Each role can have
          different permissions. A
          <b class="underline decoration-dotted">guild members</b> can have multiple
          roles. There is a limit of 100 roles per guild.
        </li>
        <li>
          <b class="underline decoration-dotted">Notice Board</b>: Every guild
          comes with a notice board where the
          <b class="underline decoration-dotted">guild owner</b>
          or a
          <b class="underline decoration-dotted">guild member</b> with required
          permissions can post announcements, updates, and other important
          information. The notice board is visible to all the
          <b class="underline decoration-dotted">guild members</b>. If a guild
          does not require a notice board, it can be disabled in the guild
          settings.
        </li>
        <li>
          <b class="underline decoration-dotted">Reception</b>: Every
          <b class="underline decoration-dotted">guild</b> is public by default.
          Should a need arise to make a guild private, the guild can opt-in for
          a reception desk. The reception desk is a join application system
          which allows a guild to set specific requirements and ask questions to
          potential members before they can join the guild. The applications are
          visible to the <b class="underline decoration-dotted">guild owner</b>
          and the <b class="underline decoration-dotted">guild members</b> with required
          permissions. Applications can be accepted or rejected by the aforementioned
          entities.
        </li>
        <li>
          <b class="underline decoration-dotted">Rooms</b>: Rooms are sections
          of a
          <b class="underline decoration-dotted">guild</b> where members can
          chat, share files, and collaborate. Rooms can be created by the
          <b class="underline decoration-dotted">guild owner</b>
          or the <b class="underline decoration-dotted">guild members</b> with
          required permissions. Rooms can be public or private. Public rooms are
          visible to all the guild members while private rooms are visible only
          to the members who have been granted access via a
          <b class="underline decoration-dotted">role</b>.
          <b class="underline decoration-dotted">Guild members</b> with required
          permissions can create, edit, and delete rooms and its entities. Rooms
          can be classified into catergories called
          <b class="underline decoration-dotted">guild wings</b>. A room can
          only be of a certain type. <em>Neocities Pagoda</em> supports the
          following types of rooms:
          <ul class="ml-8 flex flex-col gap-4 mt-4">
            <li>
              <b class="underline decoration-dotted">Libraries</b>: Libraries
              are read-only rooms comprised of
              <b class="underline decoration-dotted">Pages</b> (think of them as
              a wiki of some sort). Libraries can be used to store information that
              is important to the guild.
            </li>
            <li>
              <b class="underline decoration-dotted">Lodges</b>: Lodges are
              rooms where the guild members can chat and share files. Lodges can
              be used for general discussions, sharing of ideas, and
              collaboration.
            </li>
            <li>
              <b class="underline decoration-dotted">Galleries</b>: Galleries
              are rooms where the guild members can showcase their creations.
              Galleries can be used to display art, stories, music, and other
              creations.
            </li>
            <li>
              <b class="underline decoration-dotted">Discussions</b>:
              Discussions are rooms where the guild members can discuss various
              topics. Discussions (think of them as forums) can be used for
              debates, sharing of opinions, and discussions.
            </li>
          </ul>
        </li>
        <li>
          <b class="underline decoration-dotted">Events</b>: Events are
          activities organized by the
          <b class="underline decoration-dotted">guild staff</b>
          for the
          <b class="underline decoration-dotted">guild members</b>. Events can
          be created by any guild member with required permissions.
        </li>
        <li>
          <b class="underline decoration-dotted">Treasury</b>: Each
          <b class="underline decoration-dotted">guild</b>
          comes with a treasury where the guild accumulates the in-site virtual currency
          called <b class="underline decoration-dotted">Aura</b>. The treasury
          can be used to store the guild's earnings and spendings. The treasury
          can be accessed by the
          <b class="underline decoration-dotted">guild owner</b>
          and the <b class="underline decoration-dotted">guild members</b> with required
          permissions.
        </li>
      </ul>
    </li>
    <li>
      <b class="underline decoration-dotted">Shrines</b>: Shrines are public
      pages dedicated to a specific character, theme, or topic. Shrines can be
      used to showcase your creations, share your interests, and connect with
      other users who share similar interests. Shrines can be created by any
      user. Shrines are comprised of the following entities:
      <ul class="ml-8 flex flex-col gap-4 mt-4">
        <li>
          <b class="underline decoration-dotted">Shrine Owner</b>: A shrine
          owner is a person who created the shrine. Ownerships can be
          transferred to other site users. There can be only one owner of a
          shrine at a time. Shrines are public by default and are managed by a
          single user.
        </li>
        <li>
          <b class="underline decoration-dotted">Followers</b>: Followers are
          the users who have followed the shrine.
        </li>
        <li>
          <b class="underline decoration-dotted">Donation Box</b>: Each shrine
          comes with a donation box where the shrine accumulates the in-site
          virtual currency called
          <b class="underline decoration-dotted">Aura</b>. Shrine vistors can
          donate Aura to the shrine. The donations can be used to improve the
          shrine.
        </li>
        <li>
          <b class="underline decoration-dotted">Gallery</b>: Each shrine comes
          with a gallery where the shrine owner can showcase their creations.
          The gallery can be used to display art, stories, music, and other
          creations.
        </li>
        <li>
          <b class="underline decoration-dotted">Events</b>: Events are
          activities organized by the
          <b class="underline decoration-dotted">shrine owner</b>
          for the
          <b class="underline decoration-dotted">shrine followers</b>.
        </li>
      </ul>
    </li>
  </ul>
</div>
